package org.jymf.entity;

import java.io.Serializable;
import java.util.List;

/**
 * 该类用于酒类产品 箱内瓶数 和 托盘内箱数的参数
 * @author wfj 2015.5.18
 */
public class ProductConfig implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 箱内瓶数
	 */
	private List<String> boxCnt;
	
	/**
	 * 托盘内箱数
	 */
	private List<String> palletCnt;

	/**
	 * 公司ID，酒类具，companyIdLst 中存在的公司，画面上才显示箱、托盘的信息
	 */
	private List<String> companyId;

	public List<String> getCompanyId() {
		return companyId;
	}

	public void setCompanyId(List<String> companyId) {
		this.companyId = companyId;
	}

	public List<String> getBoxCnt() {
		return boxCnt;
	}

	public void setBoxCnt(List<String> boxCnt) {
		this.boxCnt = boxCnt;
	}

	public List<String> getPalletCnt() {
		return palletCnt;
	}

	public void setPalletCnt(List<String> palletCnt) {
		this.palletCnt = palletCnt;
	}

	
	
	
}

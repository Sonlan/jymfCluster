package org.jymf.entity;

import java.io.Serializable;
import java.util.Date;

public class BatchCount implements Serializable{
    /**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
     * 最大的消费者查询时间
     */
	private Date maxConsTime;
	
	/**
     * 最大的打包询时间
     */
    private Date maxPackTime;
    
    /**
     * 最大的激活时间
     */
    private Date maxTime;
    
    /**
     * 最小的激活查询时间
     */
    private Date minTime;

    /**
     * 最大操作时间
     */
    private Date maxOptTime;
    
	public Date getMaxOptTime() {
		return maxOptTime;
	}

	public void setMaxOptTime(Date maxOptTime) {
		this.maxOptTime = maxOptTime;
	}

	public Date getMaxConsTime() {
		return maxConsTime;
	}

	public void setMaxConsTime(Date maxConsTime) {
		this.maxConsTime = maxConsTime;
	}

	public Date getMaxPackTime() {
		return maxPackTime;
	}

	public void setMaxPackTime(Date maxPackTime) {
		this.maxPackTime = maxPackTime;
	}

	public Date getMaxTime() {
		return maxTime;
	}

	public void setMaxTime(Date maxTime) {
		this.maxTime = maxTime;
	}

	public Date getMinTime() {
		return minTime;
	}

	public void setMinTime(Date minTime) {
		this.minTime = minTime;
	}
    
}

package org.jymf.entity;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.List;

import org.jymf.utils.ImgFile;

/**
 * 和同管理_汽配模式_包标签ID=合同ID
 * @author cqs
 * @date   2014年11月18日
 */
public class Document implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 合同ID
	 */
	private BigDecimal id;
	
	/**
	 * 公司ID
	 */
	private BigDecimal companyId;

	/**
	 * 配件数量
	 */
	private BigDecimal count;
	
	/**
	 * 合同生产日期
	 */
	private String createDate;
	
	/**
	 * 车牌号
	 */
	private String carNo;

	/**
	 * 首次消费时间
	 */
	private String consTime;
	
	/**
	 * 首次消费地点
	 */
	private String consArea;
	
	/**
	 * 开始时间
	 */
    private String startDate;
	
	/**
	 * 结束时间
	 */
    private String endDate;
    
    /**
     * 合同明细
     */
    private List<DocumentDetail> labels;
    
    /**
     * 合同图片
     */
    private List<ImgFile> imgs;
    
    /**
     * 电话
     */
    private String tel;
    
	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public List<DocumentDetail> getLabels() {
		return labels;
	}

	public void setLabels(List<DocumentDetail> labels) {
		this.labels = labels;
	}

	public BigDecimal getId() {
		return id;
	}

	public void setId(BigDecimal id) {
		this.id = id;
	}

	public BigDecimal getCount() {
		return count;
	}

	public void setCount(BigDecimal count) {
		this.count = count;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getCarNo() {
		return carNo;
	}

	public void setCarNo(String carNo) {
		this.carNo = carNo;
	}

	public String getConsTime() {
		return consTime;
	}

	public void setConsTime(String consTime) {
		this.consTime = consTime;
	}

	public String getConsArea() {
		return consArea;
	}

	public void setConsArea(String consArea) {
		this.consArea = consArea;
	}
	
	
	public BigDecimal getCompanyId() {
		return companyId;
	}

	public void setCompanyId(BigDecimal companyId) {
		this.companyId = companyId;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public List<ImgFile> getImgs() {
		return imgs;
	}

	public void setImgs(List<ImgFile> imgs) {
		this.imgs = imgs;
	}
	
}

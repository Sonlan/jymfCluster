package org.jymf.entity;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;

public class LabelIndex implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private BigDecimal id;
	
	private BigDecimal status;

    private BigDecimal companyId;

    private String startTid;

    private String endTid;

    /**
     * 所属企业名称
     */
    private String name;
    
    /**
     * 标签个数
     */
    private BigDecimal count;
    
    /**
     * 追溯模式
     */
    private BigDecimal workMode;
    
    /**
     * 创建时间
     */
    private Date createDate;
    
   

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public BigDecimal getWorkMode() {
		return workMode;
	}

	public void setWorkMode(BigDecimal workMode) {
		this.workMode = workMode;
	}

	public BigDecimal getId() {
		return id;
	}

	public void setId(BigDecimal id) {
		this.id = id;
	}

    
	public BigDecimal getStatus() {
        return status;
    }

    public void setStatus(BigDecimal status) {
        this.status = status;
    }

    public BigDecimal getCompanyId() {
        return companyId;
    }

    public void setCompanyId(BigDecimal companyId) {
        this.companyId = companyId;
    }

    public String getStartTid() {
        return startTid;
    }

    public void setStartTid(String startTid) {
        this.startTid = startTid;
    }

    public String getEndTid() {
        return endTid;
    }

    public void setEndTid(String endTid) {
        this.endTid = endTid;
    }
    
    public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public BigDecimal getCount() {
		return count;
	}

	public void setCount(BigDecimal count) {
		this.count = count;
	}

}
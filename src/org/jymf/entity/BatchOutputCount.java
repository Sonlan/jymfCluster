package org.jymf.entity;

import java.io.Serializable;
import java.math.BigDecimal;

public class BatchOutputCount implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 公司ID
	 */
	private BigDecimal companyId;

	/**
	 * 产品ID
	 */
	private BigDecimal productId;
	
	/**
	 * 销售区域
	 */
	private BigDecimal salesId;
	
	/**
	 * 查询次数
	 */
	private BigDecimal queryCnt;
	
	/**
	 * 可能串货数量
	 */
	private BigDecimal acCnt;
	
	/**
	 * 首次消费查询次数
	 */
	private BigDecimal consCnt;

	/**
	 * 统计日期
	 */
	private String countDate;

	public BigDecimal getCompanyId() {
		return companyId;
	}

	public void setCompanyId(BigDecimal companyId) {
		this.companyId = companyId;
	}

	public BigDecimal getProductId() {
		return productId;
	}

	public void setProductId(BigDecimal productId) {
		this.productId = productId;
	}

	public BigDecimal getSalesId() {
		return salesId;
	}

	public void setSalesId(BigDecimal salesId) {
		this.salesId = salesId;
	}

	public BigDecimal getQueryCnt() {
		return queryCnt;
	}

	public void setQueryCnt(BigDecimal queryCnt) {
		this.queryCnt = queryCnt;
	}

	public BigDecimal getAcCnt() {
		return acCnt;
	}

	public void setAcCnt(BigDecimal acCnt) {
		this.acCnt = acCnt;
	}

	public BigDecimal getConsCnt() {
		return consCnt;
	}

	public void setConsCnt(BigDecimal consCnt) {
		this.consCnt = consCnt;
	}

	public String getCountDate() {
		return countDate;
	}

	public void setCountDate(String countDate) {
		this.countDate = countDate;
	}
	
}

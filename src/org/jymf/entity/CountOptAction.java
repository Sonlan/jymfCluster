package org.jymf.entity;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * 数据统计用，标签操作类型，包括：
 *    出库、入库、销售、销毁、采集、运输、储藏
 * @author cqs
 * @date   2014年7月8日
 */
public class CountOptAction implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 操作类型
	 */
	private BigDecimal optAction;
	
	/**
	 * 操作类型对应的数量
	 */
	private BigDecimal optCnt;

	/**
	 * 操作名称
	 */
	private String name;

	public BigDecimal getOptAction() {
		return optAction;
	}

	public void setOptAction(BigDecimal optAction) {
		this.optAction = optAction;
	}

	public BigDecimal getOptCnt() {
		return optCnt;
	}

	public void setOptCnt(BigDecimal optCnt) {
		this.optCnt = optCnt;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}

package org.jymf.utils;

/**
 * 
 * @author cqs
 * @date   2014年5月20日
 */
public class ImgFile {
	/**
	 * 文件的路径，包括文件名
	 */
	private String fileName;
	
	/**
	 * 文件序号
	 */
    private String no;

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
}
